    playertier: { type: Number, default: 0 },
    score: { type: Number, default: 0 },
    phcCoin: { type: String, default: 0 },
    joke_text: { type: String, default: "" },
    joke_url: { type: String, default: "" },
    contact: { ig: String, facebook: String, github: String, youtube: String },
    skill: { fogus: String, attitude: String, creativity: String, speed: String },
