    discord_id: String,
    rank: String,
    interest: String,
    experience: String,
    goal: String,
    course: String,
    courses: [{
        courseId: String,
        project: { name: String, url: String }
    }],
    projects: [Number],
    stats: [Number],
    remark: String,
    slogan: String,
    youtube: String,
    facebook: String,
    github: String,
    referer: String,
    profileImage: String,
    phc_status: Boolean,
    username: String,
    password: String,
    reports: [{ type: mongoose.Schema.Types.ObjectId, ref: "Report" }],
    games: [{ name: String, id: { type: mongoose.Schema.Types.ObjectId, ref: "Game" } }],
    data: Object,
    fyncid: String,
    currentScene: String,
    lastAuthentication: { type: Date, default: Date.now },
    stat: String,
    finish: String,
